clear;

v = linspace(0,20);

p = (-0.00017172 * v.^7 + 0.010832 * v.^6 ...
     -0.25304 * v.^5 + 2.65814 * v.^4 ...
     -12.75459 * v.^3 + 38.59397 * v.^2 ...
     -44.89076 * v + 11.33231).*(v > 3 & v < 14.34)...
     + 1432.501 * (14.34 <= v & v <= 20)...
     + 0.*(v > 0 & v < 3);

plot(v, p);
  